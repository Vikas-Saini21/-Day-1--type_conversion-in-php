<!-- Type Conversion in php -->
<!-- int into string -->
<?php
$x=10;
$y=(string)$x;
var_dump($y);
?>

<!--int into float-->
<?php
$x=10;
$y=(float)$x;
var_dump($y);
?>

<!-- int into array -->
<?php
$x=10;
$y=(array)$x;
var_dump($y);
?>

<!-- int into boolean -->
<?php
$x=10;
$y=(bool)$x;
var_dump($y);
?>

